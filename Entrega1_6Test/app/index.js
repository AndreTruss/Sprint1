// function sum, rest, multiply and divide

const sum = (a, b, c ) => { return (a + b + c )}
const rest = (a, b, c ) => { return a - b - c }
const multiply = (a, b, c ) => { return (a * b * c) }
const divide = (a, b ) => { return a / b }

module.exports = sum
module.exports = rest
module.exports = multiply
module.exports = divide