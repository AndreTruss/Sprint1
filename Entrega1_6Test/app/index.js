// function sum, rest, multiply and divide

function sum(a, b) { return (a + b) }
const rest = (a, b ) => { return a - b }
const multiply = (a, b ) => { return (a * b ) }
const divide = (a, b ) => { return a / b }

module.exports = sum
module.exports = rest
module.exports = multiply
module.exports = divide